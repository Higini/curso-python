# Metodos De Cadenas
# len | Longitud de cadena    |   print(len(cadena_chica))
# find  | Posicion de primer caracter   |   print(cadena.find("o"))
# rfind | Posicion del ultimo caracter  |   print(cadena.rfind("o"))
# lower | Convertir cadena a minusculas |   print(cadena_chica.lower())
# islower   | Verificar si una cadena se encuentra en minusculas    |   print( cadena.islower() )
# upper | Convertir cadena a mayusculas |   print(cadena.upper())
# isupper   | Verificar si una cadena se encuentra en mayusculas    |   print(cadena_chica.isupper())
# capitalize    |   Capitalizar una cadena  |   print(cadena.capitalize())
# replace   |   Remplazar caracteres    |   print(cadena.replace("o", "-", 2))




